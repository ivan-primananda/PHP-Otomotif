                  Cara membersihkan plafon mobil ada beberapa cara, seperti hal nya di bawah ini ada cara membersihkan plafon mobil sesuai dengan noda yang ada di plafon mobil :<br>

1. Membersihkan Plafon Berdebu<br>

Membersihkan Plafon Berdebu dengan sikat tanpa air, jika plafon berdebu maka Anda cukup menyikatnya dengan lembut tanpa air. Kemudian, sikat plafon dengan gerakan tangan searah menggunakan sikat gigi yang lembut. Lakukanlah secara perlahan. Menyikat plafon yang berdebu dengan air atau campuran air sabun atau detergen ternyata akan membuatnya semakin kotor.

Karena, debu di plafon akan menyatu dengan air. Semakin di sikat justru kotorannya akan semakin menyebar. Bukan bersih yang akan kita dapat, tapi noda yang semakin membandel di atas plafon. Cara ini berlaku jika plafon hanya berdebu saja bukan untuk mengurangi noda.<br>

2. Menghilangkan plafon yang Bernoda Ringan<br>

Menghilangkan plafon yang bernoda ringan dengan menggunakan sikat lembab. Cara membersihkan plafon mobil yang memiliki noda adalah dengan menggunakan sikat lembab dengan sedikit detergen. Cara ini dilakukan jika ada noda ringan pada plafon. Gunakan sikat gigi bekas dengan bulu lembut dan detergen yang sedikit mengandung busa. Caranya:

<br>-Tuang detergen pada air (jangan diaduk). Tunggu 3 menit agar partikel nya bereaksi dengan air.
-Celup sikat ke dalam larutan detergen kemudian angkat.<br>
-Gerakan sikat ke atas dan ke bawah agar larutan detergen terbuang dan sikat menjadi lembab.<br>
-Sikat perlahan noda di plafon dengan lembut dan searah.<br>
-Jangan mengaduk larutan detergen, agar tidak berbusa. Jika sikat berbusa dan menyentuh plafon maka akan menghasilkan flek yang tidak bisa hilang. Jika begini, maka bukan menyelesaikan masalah, namun menimbulkan masalah baru.<br>

3. Plafon dengan Noda Pekat<br>

Cara membersihkan plafon mobil yang memiliki noda pekat atau membandel ini adalah dengan menggunakan cairan fabric. Namun, ada caranya untuk menggunakan cairan ini. Sebelum melangkah lebih lanjut kesana, anda perlu menyiapkan beberapa peralatan untuk membersihkan plafon, yaitu:<br>

-Kain yang lembut dan halus. Anda dapat menggunakan kain berjenis microfiber agar plafon mobil tidak rusak saat dibersihkan.<br>
-Cairan pembersih. Pilihlah cairan khusus plafon mobil (fabric). Anda dapat membeli cairan ini di bengkel atau toko tertentu.<br>
-Siapkan air secukupnya. Gunakan air tanpa campuran deterjen, sabun ataupun yang semisalnya. Cukup sediakan air bening saja.<br>
-Setelah anda menyiapkan semua peralatan, lalu bagaimana cara membersihkan plafon mobilnya? Berikut ini langkah yang dapat kita lakukan untuk membersihkan plafon mobil.<br>

-Basahi kain dengan air, peras sampai kering hingga tidak ada air yang menetes lagi. Pastikan kain terperas sampai kering, karena kain yang basah dapat menyebabkan plafon menjadi lembab. Hal ini akan mempercepat timbulnya bakteri dan jamur sehingga menimbulkan bau yang tidak sedap.<br>
-Usapkan kain pada plafon secara perlahan. Tujuannya, agar plafon tidak sobek.<br>
-Semprotkan cairan pembersih fabric ke kain. Lalu, gosok plafon dengan menggunakan kain tersebut. Cairan pembersih fabric berguna untuk menghilangkan noda yang membandel pada plafon mobil anda.<br>
4. Gunakan Vacuum Cleaner Sesekali<br>

Cobalah untuk membersihkan plafon mobil dengan vacuum cleaner. Penggunaan vacuum cleaner untuk membersihkan plafon ini ada caranya, tidak bisa membersihkan nya secara sembarangan. Jika salah menggunakan, justru dapat membuat mobil menjadi rusak. Syarat membersihkan plafon mobil dengan vacuum cleaner ini adalah dengan mengatur daya serap alat pembersih otomatis satu ini, yaitu dengan menggunakan daya serap ringan. Penggunaan dengan daya serap tinggi justru merusak plafon dan menjadi tidak bagus.

Demikianlah cara – cara yang dapat anda lakukan untuk membersihkan plafon pada mobil. Jenis-jenis bahan plafon pun sebenarnya berbeda-beda. Untuk itu, dalam membersihkan nya pun ada cara tersendiri, menyesuaikan dengan bahan plafon. Ribet yah? Tidak juga sebenarnya, jika kita mengetahui dengan baik bagaimana merawat sesuatu tentu hal itu menjadi mudah. Lebih mudah merawatnya daripada sudah terlanjur rusak dan perlu diperbaiki. Tentu akan mengeluarkan pengeluaran lebih.<br>

5. Membersihkan Plafon Mobil Berbahan Fabric<br>

Cara yang bisa anda lakukan untuk membersihkan plafon mobil jenis ini adalah dengan terlebih dahulu menyiapkan cairan pembersih fabric (dari hewan) dan kain berjenis mikrofiber. Setelah itu, basahi kain tersebut dengan cairan fabric.Sebaiknya, anda tidak langsung menyemprotkan cairan fiber ke plafon, karena hal ini akan membuat kotor permukaan plafon.

Proses perawatan plafon anda akan menjadi sia-sia. Menyemprotkan cairan fiber ke kain berfungsi agar plafon tidak terlalu basah saat dibersihkan. Seperti yang telah disebutkan di atas, plafon tidak boleh lembab dan terlalu basah yang akan menimbulkan banyak kotoran dan bakteri.

Ketika proses membersihkan plafon, serat halus yang ada pada kain plafon akan bergerak. Oleh karena itu, lebih baik anda membersihkan nya searah agar plafon tidak rontok. Setelah anda membersihkan nya dengan kain dan cairan fabric dan permukaan plafon telah kering, tengoklah arah serat nya. Pastikan serat – serat dalam plafon itu rapi dan menyatu dengan sekelilingnya.Untuk mengatur arah serat, anda dapat menggunakan kain mikrofiber dan hindari penggunaan sikat yang dapat membuat plafon mobil tertarik oleh bulu sikat yang dapat mengakibatkan kerontokan.
<br>
6. Membersihkan Plafon dari Beludru<br>

Plafon mobil berbahan beludru sangat sulit untuk dibersihkan. Dalam merawatnya pun perlu hati-hati karena material dalam beludru berlapis-lapis. Sehingga, ada cara khusus dalam merawat dan membersihkannya.Pertama, Bersihkan plafon dengan mengunakan air secukupnya. Kemudian, gosok perlahan dengan lap yang setengah basah.
Lapisan luar plafon berasal dari kain wol yang jika terkena air sedikit saja dapat mengakibatkan lapisan wol itu menjadi lebih lembab. Plafon yang lembab akan menimbulkan masalah baru bagi mobil, yaitu efek bau menyengat.Selanjutnya, Kelembaban tersebut bisa menyebabkan jamur.

Oleh karena itu, Anda perlu segera membersihkan nya sebelum lembab semakin meluas.Lalu, Bagaimana jika plafon mobil terlanjur kotor dan rusak?Untuk hal ini, sayangnya Anda tidak dapat memperbaikinya sendiri. Anda perlu membawa mobil anda ke bengkel untuk memperbaiki plafon mobil.Plafon mobil sangat berpengaruh dan perlu dirawat karena berhubungan dengan para penumpang dan pengendara mobil.Itulah berbagai cara membersihkan plafon mobil.
