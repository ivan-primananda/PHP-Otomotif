Selama ini yang cukup sering dilakukan oleh pengendara mobil adalah langsung meghidupkan AC mobil setelah mobil berjalan tanpa membuka kaca jendela. Membuka kaca jendela saat mobil dan ac dinyalakan disarankan, karena untuk membuang udara di kabin setelah mobil di parkir yang terkena sinar matahari langsung.<br>

Mengapa hal ini perlu dilakukan? karena radiasi sinar matahari menyebabkan reaksi kimia antara material plastik, pengharum ruangan, bahan pelapis jok dan bahan lain di kabin dengan udara panas menghasilkan berbagai zat baru, satu diantaranya benzena. Bagi sebagian orang mungkin tidak menjadi persoalan kala menghirup zat tersebut, tetapi bagia sebagian lainnya akan berpengaruh besar, karena tanpa disadari pengemudi dan penumpang yang ada didalamnya akan terasa mengantuk , pusing, atau bahkan lemas dan tidak konsentrasi.<br>

