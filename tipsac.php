Jika AC mobil baru dihidupkan saat mobil telah melaju atau di saat putaran mesin tinggi sehingga putaran pulley juga tinggi, hal ini akan terjadi gesekan yang hebat antara pressure plate dengan pulley. Jangka panjangnya kedua komponen tersebut menjadi cepat haus, dan jika keduanya haus, maka putaran mesin untuk memberikan tenaga bagi compressor untuk menekan Freon yang diubah menjadi udara dingin menjadi tidak maksimal. Akibatnya, AC tidak dingin, kondisi ini berpengaruh pada komponen lain dan rusak.<br>

Selain itu saat AC dihidupkan akan terdengar suara berisik karena gesekan antara pulley dan pressure plate kadang cepat dan kadang lambat. Sebab, permukaan pressure plate juga bergelombang atau tidak rata akibat gesekan keras yang terjadi berulangkali saat pertama AC dihidupkan.
